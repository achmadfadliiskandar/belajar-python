# tipe data dalam python
# str , int, float, complex
# list, tuple, range , dict
# set, frozenset , bool , bytes, 
# bytearray , memoryview
# nonetype

# integer / int

x = 10
print(type(x))

# string / str
negara = "Indonesia"
print(type(negara))

# float 
uang = 100.000
print(type(uang))

# boolean 
benar_salah = True
print(type(benar_salah))

# set

listsiswa = {"andi","budi","cakra"}
print(type(listsiswa))

#list / tuple sebenarnya sama cma beda simbol saja
saudara = ['fadlan','rani','faiz']
print(type(saudara))

saudara = ('fadlan','rani','faiz')
print(type(saudara))

# bonus tipe data dict

nama_lengkap = {"NamaDepan" : "Achmad" , "NamaTengah" : "Fadli" , "NamaBelakang" : "Iskandar"}
print(type(nama_lengkap))